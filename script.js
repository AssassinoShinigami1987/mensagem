// Variáveis globais
let currentPoemIndex = 0;
let currentSlideIndex = 0;
let audioEnabled = false;
let audioElement;
let slideInterval; // Intervalo para mudança automática de slides

// Elementos DOM
const startScreen = document.getElementById('start-screen');
const poemScreen = document.getElementById('poem-screen');
const endScreen = document.getElementById('end-screen');
const gothicScreen = document.getElementById('gothic-screen');
const startBtn = document.getElementById('start-btn');
const restartBtn = document.getElementById('restart-btn');
const gothicRestartBtn = document.getElementById('gothic-restart-btn');
const prevBtn = document.getElementById('prev-btn');
const nextBtn = document.getElementById('next-btn');
const finalizeBtn = document.getElementById('finalize-btn');
const finalizeControls = document.querySelector('.finalize-controls');
const poemTitle = document.getElementById('poem-title');
const poemText = document.getElementById('poem-text');
const currentDay = document.getElementById('current-day');

// Inicialização
document.addEventListener('DOMContentLoaded', function() {
    audioElement = document.getElementById('background-audio');
    setupEventListeners();
    loadPoem(0);
    // Configurar áudio para tocar automaticamente
    audioEnabled = true;
});

// Configurar event listeners
function setupEventListeners() {
    startBtn.addEventListener('click', startJourney);
    restartBtn.addEventListener('click', restartJourney);
    gothicRestartBtn.addEventListener('click', restartJourney);
    prevBtn.addEventListener('click', previousPoem);
    nextBtn.addEventListener('click', nextPoem);
    finalizeBtn.addEventListener('click', showGothicScreen);
    
    // Navegação por teclado
    document.addEventListener('keydown', function(e) {
        if (e.key === 'ArrowLeft') {
            previousPoem();
        } else if (e.key === 'ArrowRight') {
            nextPoem();
        }
    });
}

// Funções de navegação
function startJourney() {
    startScreen.classList.remove('active');
    poemScreen.classList.add('active');
    // Tocar áudio automaticamente quando começar
    enableAudio();
    playAudio();
    // Configurar primeira imagem e iniciar slideshow automático
    updateSlideshow(0);
    startAutoSlideshow();
}

function restartJourney() {
    endScreen.classList.remove('active');
    gothicScreen.classList.remove('active');
    startScreen.classList.add('active');
    currentPoemIndex = 0;
    currentSlideIndex = 0;
    loadPoem(0);
    // Parar áudio ao reiniciar
    stopAudio();
    // Parar slideshow automático
    stopAutoSlideshow();
}

function previousPoem() {
    if (currentPoemIndex > 0) {
        currentPoemIndex--;
        loadPoem(currentPoemIndex);
    }
}

function nextPoem() {
    if (currentPoemIndex < poemas.length - 1) {
        currentPoemIndex++;
        loadPoem(currentPoemIndex);
    }
}

function showGothicScreen() {
    poemScreen.classList.remove('active');
    gothicScreen.classList.add('active');
    // Manter o áudio tocando
    stopAutoSlideshow();
}

// Carregar poema
function loadPoem(index) {
    const poem = poemas[index];
    poemTitle.textContent = poem.titulo;
    poemText.textContent = poem.texto;
    currentDay.textContent = poem.dia;
    
    // Atualizar botões
    prevBtn.disabled = index === 0;
    
    // Se for o último poema, mostrar botão finalizar
    if (index === poemas.length - 1) {
        nextBtn.style.display = 'none';
        finalizeControls.style.display = 'block';
    } else {
        nextBtn.style.display = 'inline-block';
        finalizeControls.style.display = 'none';
    }
    
    // Atualizar contador
    currentPoemIndex = index;
}

// Atualizar slideshow
function updateSlideshow(poemIndex) {
    // Remover classe active de todos os slides
    const slides = document.querySelectorAll('.slide');
    slides.forEach(slide => slide.classList.remove('active'));
    
    // Adicionar classe active ao slide correspondente
    const slideIndex = poemIndex % slides.length;
    slides[slideIndex].classList.add('active');
    currentSlideIndex = slideIndex;
}

// Slideshow automático
function startAutoSlideshow() {
    // Limpar intervalo existente se houver
    if (slideInterval) {
        clearInterval(slideInterval);
    }
    
    // Iniciar novo intervalo - mudar slide a cada 15 segundos
    slideInterval = setInterval(function() {
        const slides = document.querySelectorAll('.slide');
        const totalSlides = slides.length;
        
        // Remover classe active do slide atual
        slides[currentSlideIndex].classList.remove('active');
        
        // Avançar para o próximo slide
        currentSlideIndex = (currentSlideIndex + 1) % totalSlides;
        
        // Adicionar classe active ao novo slide
        slides[currentSlideIndex].classList.add('active');
    }, 15000); // 15 segundos
}

function stopAutoSlideshow() {
    if (slideInterval) {
        clearInterval(slideInterval);
        slideInterval = null;
    }
}

// Controle de áudio
function enableAudio() {
    audioEnabled = true;
    if (poemScreen.classList.contains('active')) {
        playAudio();
    }
}

function stopAudio() {
    audioEnabled = false;
    if (audioElement) {
        audioElement.pause();
        audioElement.currentTime = 0;
    }
}

function playAudio() {
    if (audioElement && audioEnabled) {
        audioElement.play().catch(e => {
            console.log('Erro ao tocar áudio:', e);
        });
    }
}

// Loop do áudio
audioElement.addEventListener('ended', function() {
    if (audioEnabled) {
        audioElement.currentTime = 0;
        audioElement.play();
    }
});

// Efeitos visuais adicionais
function addHeartEffect() {
    const heart = document.createElement('div');
    heart.innerHTML = '❤️';
    heart.style.position = 'fixed';
    heart.style.left = Math.random() * window.innerWidth + 'px';
    heart.style.top = window.innerHeight + 'px';
    heart.style.fontSize = '2rem';
    heart.style.pointerEvents = 'none';
    heart.style.zIndex = '1000';
    heart.style.transition = 'all 3s ease-out';
    
    document.body.appendChild(heart);
    
    setTimeout(() => {
        heart.style.top = '-100px';
        heart.style.opacity = '0';
    }, 100);
    
    setTimeout(() => {
        document.body.removeChild(heart);
    }, 3000);
}

// Adicionar efeito de coração a cada 5 segundos
